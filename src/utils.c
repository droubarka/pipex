#include "pipex.h"

void	close_stdio(int *stdio)
{
	if (stdio[0] != -1)
		close(stdio[0]);
	if (stdio[1] != -1)
		close(stdio[1]);

	stdio[0] = -1;
	stdio[1] = -1;
}
