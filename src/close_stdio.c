#include "pipex.h"

int	close_stdio(int *stdio)
{
	int	i;

	i = 0;
	while (i < 2)
	{
		if (stdio[i] != -1 && close(stdio[i]) == -1)
		{
			terminate("close", -1);
		}
		i++;
	}
	return (0);
}
